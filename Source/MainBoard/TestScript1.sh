#!/bin/bash

export DISPLAY=:0.0

UnableTTYS(){
	cd /etc/systemd/system/getty.target.wants/
	systemctl mask serial-getty@ttyS0.service
	systemctl mask serial-getty@ttyS1.service 
}


Init_USB(){
	cd /data
	usb=_usb

	for sd in sda sdb sdc sdd sde sdf
	do
		if [ -d "/data/$sd$usb" ]
		then
			umUsb=$(mount |grep /dev/$sd | cut -d  '1' -f1)
			if [ "$umUsb" = "/dev/$sd" ];
			then
				rm -r /data/$sd$usb
			fi
		fi
	done

	echo "Init success."
}

